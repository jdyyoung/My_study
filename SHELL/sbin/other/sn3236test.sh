#!/bin/sh

i=0

LED_init(){
	i2cset -y 1 0x3c 0x4f 0x00
	i2cset -y 1 0x3c 0x00 0x01
}

LED1_turn_on(){
	i2cset -y 1 0x3c 0x26 0x01
	i2cset -y 1 0x3c 0x27 0x01
	i2cset -y 1 0x3c 0x28 0x00

	i2cset -y 1 0x3c 0x01 0xff
	i2cset -y 1 0x3c 0x02 0xff
	i2cset -y 1 0x3c 0x03 0xff

	i2cset -y 1 0x3c 0x25 0x25
}

LED1_turn_off(){
	i2cset -y 1 0x3c 0x26 0x00
	i2cset -y 1 0x3c 0x27 0x00
	i2cset -y 1 0x3c 0x28 0x00

#	i2cset -y 1 0x3c 0x01 0x00
#	i2cset -y 1 0x3c 0x02 0x00
#	i2cset -y 1 0x3c 0x03 0x00

	i2cset -y 1 0x3c 0x25 0x25
}

LED_init

while [ $i -le 10 ];do
	LED1_turn_on
	sleep 1
	LED1_turn_off
	sleep 1
	i=$((i+1))
done
